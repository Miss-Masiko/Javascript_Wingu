let lunch = "pizza";

switch(lunch) {

    case "cereal":
        document.write("Cereal? you had breakfast for lunch!")
        break;

    case "salad":
        document.write("Salad makes for a healthy lunch!")
        break;
    case "pizza":
        document.write("Pizza is yummy!")
        break;

    default:
        document.write("It looks like you skipped lunch")
        break;


}

